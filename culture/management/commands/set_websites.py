class Command(BaseCommand):
    help = '공식 홈페이지 URL을 Place.website에 저장합니다'

    def add_arguments(self, parser):
        parser.add_argument('--overwrite', action='store_true',
                            help='이미 website가 있는 장소도 덮어쓰기')

    def handle(self, *args, **options):
        overwrite = options['overwrite']
        updated = skipped = 0

        for keyword, url in WEBSITE_MAP:
            qs = Place.objects.filter(name__icontains=keyword)
            if not overwrite:
                qs = qs.filter(website='')
            count = qs.update(website=url)
            if count:
                self.stdout.write(f'  [{count}건] {keyword} → {url}')
                updated += count
            else:
                skipped += 1

        # URL 없는 장소는 나무위키로 자동 설정
        # 장소명의 지역 구분자 괄호 제거: "달마사(서울)" → "달마사"
        namu_count = 0
        for place in Place.objects.filter(website=''):
            base_name = re.sub(r'\s*\(.*?\)', '', place.name).strip()
            place.website = f'https://namu.wiki/w/{quote(base_name)}'
            place.save(update_fields=['website'])
            namu_count += 1
        if namu_count:
            self.stdout.write(f'  [{namu_count}건] 나무위키 자동 설정 (괄호 제거)')
            updated += namu_count

        self.stdout.write(self.style.SUCCESS(
            f'\n완료: {updated}개 업데이트, {skipped}개 키워드 미매칭'
        ))
